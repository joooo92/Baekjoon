package While_For;

import java.util.Scanner;

public class Bj_01 {
	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		
		int n = scan.nextInt();
		
		
		for (int a = 1; a <= 9; a++) {
			
			System.out.println(n + " * " + a + " = " + n*a);
			
			
		}
		
		
		
		
	}

}
